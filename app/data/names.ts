// Fisher-Yates shuffle for true randomness
function fisherYatesShuffle<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

// Function to get random names from the collection
export function getRandomNames(count: number = 10): string[] {
  if (count >= names.length) {
    return [...names]; // Return all names if requested count exceeds available names
  }

  // Shuffle the array and take the first 'count' items
  const shuffled = fisherYatesShuffle(names);
  return shuffled.slice(0, count);
}

// Function to get all names
export function getAllNames(): string[] {
  return [...names];
}

// Function to get names by approximate cultural origin
export function getNamesByOrigin(origin: string, count: number = 50): string[] {
  // This is a simplified example - you could extend this with more sophisticated filtering
  const originMappings: { [key: string]: number[] } = {
    english: [0, 50],
    spanish: [50, 150],
    french: [150, 250],
    german: [250, 350],
    // Add more mappings as needed
  };

  if (origin in originMappings) {
    const [start, end] = originMappings[origin];
    const subset = names.slice(start, end);
    const shuffled = fisherYatesShuffle(subset);
    return shuffled.slice(0, Math.min(count, subset.length));
  }

  return getRandomNames(count);
}
